export function translate(locale: LocaleCode, key: string, ...args: Array<string | number>): string {
  const table = translations[locale] || translations["pt-BR"];
  const template = table[key] || translations["pt-BR"][key] || key;
  let out = template;
  for (const value of args) {
    out = out.replace("%s", String(value));
  }
  return out;
}
